#!/usr/bin/env python3
"""
Parse Fastp JSON Results for Insert Size Analysis

This script analyzes the JSON reports generated by fastp to extract
detailed insert size information using overlap analysis.

Author: Generated by Lingma
Date: 2026-05-18
"""

import json
import os
import sys
import argparse
from pathlib import Path


def calculate_insert_size_from_overlap(read1_len, read2_len, merged_len):
    """
    Calculate insert size from overlap information.
    
    For overlapping paired-end reads:
    insert_size = read1_length + read2_length - overlap_length
    
    Where overlap_length can be estimated from merged read length.
    """
    if merged_len > 0:
        # The merged read length represents the combined non-overlapping regions
        # plus the overlapping region once
        # So: merged_len = read1_len + read2_len - overlap_len
        # Therefore: overlap_len = read1_len + read2_len - merged_len
        # And: insert_size = read1_len + read2_len - overlap_len = merged_len
        return merged_len
    else:
        # No overlap detected, insert size > read1_len + read2_len
        return read1_len + read2_len


def parse_fastp_report(json_file):
    """Parse fastp JSON report and extract insert size metrics."""
    try:
        with open(json_file, 'r') as f:
            data = json.load(f)
    except (FileNotFoundError, json.JSONDecodeError) as e:
        print(f"Error reading {json_file}: {e}")
        return None
    
    metrics = {}
    
    # Basic sequencing metrics
    summary = data.get('summary', {}).get('before_filtering', {})
    metrics['total_reads'] = summary.get('total_reads', 0) // 2  # Paired-end
    metrics['read1_mean_length'] = summary.get('read1_mean_length', 0)
    metrics['read2_mean_length'] = summary.get('read2_mean_length', 0)
    metrics['q30_rate'] = summary.get('q30_rate', 0.0)
    
    # Overlap/Merge metrics
    merge = data.get('merge', {})
    metrics['merged_reads'] = merge.get('merged_reads', 0)
    metrics['merge_rate'] = merge.get('merge_rate', 0.0)
    metrics['average_merged_length'] = merge.get('average_merged_length', 0)
    
    # Calculate insert size estimates
    r1_len = metrics['read1_mean_length']
    r2_len = metrics['read2_mean_length']
    merged_len = metrics['average_merged_length']
    
    if metrics['merged_reads'] > 0:
        # We have overlapping reads, can estimate actual insert size
        metrics['estimated_insert_size'] = calculate_insert_size_from_overlap(
            r1_len, r2_len, merged_len
        )
        metrics['insert_size_type'] = 'estimated'
    else:
        # No overlaps, insert size is larger than read lengths
        metrics['estimated_insert_size'] = r1_len + r2_len
        metrics['insert_size_type'] = 'minimum_bound'
    
    return metrics


def generate_summary_report(results_dir, output_file):
    """Generate a comprehensive summary report."""
    json_files = list(Path(results_dir).glob("*/fastp.json"))
    
    if not json_files:
        print(f"No fastp JSON files found in {results_dir}")
        return
    
    all_metrics = {}
    for json_file in sorted(json_files):
        sample_name = json_file.parent.name
        print(f"Parsing {sample_name}...")
        metrics = parse_fastp_report(str(json_file))
        if metrics:
            all_metrics[sample_name] = metrics
    
    # Write detailed summary
    with open(output_file, 'w') as f:
        f.write("FASTP OVERLAP-BASED INSERT SIZE ANALYSIS\n")
        f.write("=" * 50 + "\n\n")
        
        f.write("Methodology:\n")
        f.write("- Uses fastp's overlap detection to identify merged read pairs\n")
        f.write("- For merged reads: insert_size ≈ merged_read_length\n")
        f.write("- For non-merged reads: insert_size > read1_length + read2_length\n\n")
        
        f.write("Sample Summary:\n")
        f.write("-" * 100 + "\n")
        header = f"{'Sample':<15} {'Total Reads':<12} {'R1 Len':<8} {'R2 Len':<8} "
        header += f"{'Merged':<10} {'Merge %':<8} {'Est Insert':<12} {'Type'}"
        f.write(header + "\n")
        f.write("-" * 100 + "\n")
        
        for sample in sorted(all_metrics.keys()):
            m = all_metrics[sample]
            total_reads = m['total_reads']
            r1_len = m['read1_mean_length']
            r2_len = m['read2_mean_length']
            merged = m['merged_reads']
            merge_pct = m['merge_rate'] * 100
            est_insert = m['estimated_insert_size']
            insert_type = m['insert_size_type']
            
            row = f"{sample:<15} {total_reads:<12} {r1_len:<8} {r2_len:<8} "
            row += f"{merged:<10} {merge_pct:<8.2f} {est_insert:<12} {insert_type}"
            f.write(row + "\n")
    
    print(f"\nDetailed summary written to: {output_file}")
    
    # Print to console
    print("\n" + "=" * 100)
    print("FASTP INSERT SIZE ANALYSIS SUMMARY")
    print("=" * 100)
    print(header)
    print("-" * 100)
    for sample in sorted(all_metrics.keys()):
        m = all_metrics[sample]
        total_reads = m['total_reads']
        r1_len = m['read1_mean_length']
        r2_len = m['read2_mean_length']
        merged = m['merged_reads']
        merge_pct = m['merge_rate'] * 100
        est_insert = m['estimated_insert_size']
        insert_type = m['insert_size_type']
        
        row = f"{sample:<15} {total_reads:<12} {r1_len:<8} {r2_len:<8} "
        row += f"{merged:<10} {merge_pct:<8.2f} {est_insert:<12} {insert_type}"
        print(row)


def main():
    parser = argparse.ArgumentParser(
        description='Parse fastp JSON reports for insert size analysis'
    )
    parser.add_argument(
        '--results-dir',
        required=True,
        help='Directory containing fastp results (with sample subdirectories)'
    )
    parser.add_argument(
        '--output',
        default='fastp_insert_size_detailed_summary.txt',
        help='Output summary file name'
    )
    
    args = parser.parse_args()
    
    # Verify results directory exists
    if not os.path.exists(args.results_dir):
        print(f"Error: Results directory not found: {args.results_dir}")
        sys.exit(1)
    
    generate_summary_report(args.results_dir, args.output)


if __name__ == "__main__":
    main()