
# 这是一个常见的 Linux 误区：cd 是一个 Shell 内建命令，而不是一个独立的可执行程序，因此 sudo cd 是无效的。 
# sudo 只能启动一个新的进程，而 cd 必须在当前 Shell 中运行才能改变目录。
# 既然你有完全的 sudo 权限，有以下几种方法可以进入该目录并操作：
# 方法 1：直接变成该用户（最推荐）
# 这种方式最省事，你不仅能进入目录，还能以该用户的身份执行后续命令，且环境配置（Alias、路径等）都会生效。
sudo su - lhn
# 执行后，你会直接进入 /home/lhn，且身份变为 lhn。
# 输入 exit 可以退回到你自己的账号。
# 方法 2：先变成 root，再进入
# 如果你想拥有最高权限（root）并在各个目录间自由穿梭：
sudo -i
# # 此时你已经是 root 了
# cd /home/lhn
# 方法 3：使用 sudo -s（保持当前环境但提升权限）
sudo -s
# cd /home/lhn